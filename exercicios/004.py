n=input('digite algo: ')
print('o tipo primitivo desse valor é', type(n))
print('é composto somente de espaços.',n.isspace())
print('é numero.',n.isnumeric())
print('é alfabetico.',n.isalpha())
print('é alfanumerico.',n.isalnum())
print('é somente maiusculas.',n.isupper())
print('é somente minusculas.',n.istitle())



